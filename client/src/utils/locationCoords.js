// Case-insensitive lookup — returns [longitude, latitude] or null.
export function getCoords(location) {
  if (!location) return null
  const exact = COORDS[location]
  if (exact) return exact
  const lower = location.toLowerCase()
  const match = Object.entries(COORDS).find(([k]) => k.toLowerCase() === lower)
  return match ? match[1] : null
}
